#include <iostream>

using namespace std;

int main()
{
    // declared 3 variables to store user inputs 0.5/1
    unsigned short n1,n2,n3,sum=0;
    
    for (int i = 1; i <= 25; i++)
    {
        // prompting user for 3 numbers to add 1/1
        cout << "Enter 3 numbers \n";
        cin >> n1 >> n2 >> n3;

        // outputting the result of the summation process 2/2
        sum = n1 + n2 + n3;
        cout << "----------\n" << sum << "\n";
    }
}
